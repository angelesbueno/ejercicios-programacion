public class Ejercicio1 {
  public static void main(String [] args){
  
    int x=144;
    int y=999;
    int suma=x+y;
    int resta=x-y;
    int multiplicacion=x*y;
    int division=x/y;
    
    
    System.out.println("La variable x es "+x);
    System.out.println("La variables y es "+y);
    System.out.println("La suma de las variables es " +suma);
    System.out.println("La resta de las variables es "+resta);
    System.out.println("La multiplicación de las variables es "+multiplicacion);
    System.out.println("La división de las variables es "+division);
    
  }
}
  
    
    
    
